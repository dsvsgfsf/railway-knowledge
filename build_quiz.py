def format_questions(course_name, questions):
    """将Python题目列表格式化为JS代码"""
    lines = [f"addQuestions('{courseName}', ["]
    for q in questions:
        if q["type"] == "single":
            opts_str = ",".join(['"'+o+'"' for o in q["opts"]])
            lines.append(f'{{type:"{q["type"]}",q:"{q["q"]}",opts:[{opts_str}],ans:"{q["ans"]}",exp:"{q["exp"]}"}},')
        elif q["type"] == "multi":
            opts_str = ",".join(['"'+o+'"' for o in q["opts"]])
            ans_str = ",".join(['"'+a+'"' for a in q["ans"]])
            lines.append(f'{{type:"{q["type"]}",q:"{q["q"]}",opts:[{opts_str}],ans:[{ans_str}],exp:"{q["exp"]}"}},')
        elif q["type"] == "judge":
            ans_val = "true" if q["ans"] else "false"
            lines.append(f'{{type:"{q["type"]}",q:"{q["q"]}",ans:{ans_val},exp:"{q["exp"]}"}},')
    lines.append("]);")
    return "\n".join(lines)

